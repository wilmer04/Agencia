   @Override
    public void update(Observable obs, Object obj) {
    ClientesDB cliDB = (ClientesDB) obs;
    this.setTitle("Profesi√≥n: " + cliDB.getProfesion());
    graficar(cliDB.getTotalHombres(), cliDB.getTotalMujeres(), cliDB.getProfesion());
    }
    public void graficar (int hom, int muj, String pro){
        JFreeChart grafico = null;
        DefaultCategoryDataset datos = new DefaultCategoryDataset();
        ClientesDB cliDB = new ClientesDB();
        int dato1 = hom;
        int dato2 = muj;
        
        datos.addValue(dato1, "Hombres", "Hombres");
        datos.addValue(dato2, "Mujeres", "Mujeres");     
        grafico = ChartFactory.createBarChart("Grafica: "," "," ", datos);
        
        ChartPanel cPanel = new ChartPanel(grafico);
        JFrame informacion = new JFrame("Profesion: "+ pro);
        informacion.getContentPane().add(cPanel);
        informacion.pack();
        informacion.setVisible(true);                
    }
